package Ch3_Conditional_Statements;

public class SwtichOP {
    public static void main(String[] args) {
        int num = 4;
        switch (num) {
            case 1:
                System.out.println("Hello");
                break;
            case 2 :
            System.out.println("World");
            break;
            case 3 :
            System.out.println("Welcome");
            break;

            default:
            System.out.println("bye");
                break;
        }
    }
}
